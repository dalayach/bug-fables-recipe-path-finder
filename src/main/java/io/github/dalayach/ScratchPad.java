   public enum NestedEnum
   {
   
      A(null, 10),
      B(A, 100),
      C(B, 1000),
   
      ;
   
      final NestedEnum nestedEnum;
      final int num;
   
      NestedEnum(NestedEnum e, int num)
      {
      
         if (e != null)
         {
         
            this.nestedEnum = e;
            this.num = e.getNum();
         
         }
         
         else
         {
         
            this.nestedEnum = null;
            this.num = num;
         
         }
      
      }
      
      public int getNum()
      {
      
         return this.num;
      
      }
      
      public String toString()
      {
      
         return "\nname = " + this.name()
            + "\nnestedEnum.name() = " + (this.nestedEnum == null ? null : this.nestedEnum.name())
            + "\nnum = " + this.num
            ;
      
      }
   
   }

